import math

m = float(input('Введите вес в килограммах: '))
h = float(input('Введите рост в метрах: '))

H = h ** 2

l = m / H

if l < 18.5:
    r = 'недастаток веса'
if l >=18.5 and l < 25:
    r = 'норма'
if l >= 25 and l < 30:
    r = 'избыточная масса'
if l >= 30 and l < 35:
    r  = 'ожирение 1 степени'
if l >= 35 and l < 40:
    r = 'ожирение 2 степени'
if l >= 40:
    r = 'ожирение 3 степени'

L = math.trunc(l)


print('Ваш ИМТ:', L, r)
